    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
       int transactionId=Integer.parseInt(request.getParameter("transaction"));
        deleteTransactionById(request, response,transactionId);
    }
    
    protected void deleteTransactionById(HttpServletRequest request, HttpServletResponse response,int transactionId) throws IOException
    {
        PrintWriter writer=response.getWriter();
        try {
                Connection conn = Credential.getConnection();
                if (conn != null) {
                    writer.println("database connection successful.");

                }

                String query = "delete from transactions where tran_id=?";
                PreparedStatement pstmt = conn.prepareStatement(query);
                pstmt.setInt(1, transactionId);
                
                pstmt.execute();
                conn.close();
                System.out.println("User removed");
                response.sendRedirect("view-account-detail.jsp?account=1&success=Transaction deleted.");
            } catch (SQLException ex) {
                Logger.getLogger(RegisterUser.class.getName()).log(Level.SEVERE, null, ex);
            }
    }

